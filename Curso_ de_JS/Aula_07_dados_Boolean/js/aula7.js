/* 
Tipo de Dados

No JavaScript temos diversos tipos de dados,
 os mais comuns e ultilizados são:
 number , string , boolean, null  undefined, object;
 podemos verificar o tipo de dado com o operador typeof;

BOOLEAN

O Boolean representa um valor verdadeiro ou falso;
Comparações resultam em Booleans;
Podemos tambem atribuir um true ou false para uma variavel
e ela terá o tipo Boolean;

 */

var verdadeiro = true;

console.log(verdadeiro);
console.log(typeof verdadeiro);

console.log(typeof true);
console.log(typeof false);

var falso = false;
console.log(falso);
console.log(typeof falso);
